    private Alumnos asignarDatos(java.awt.event.ActionEvent evt, int viveCon, int carrera, String clave, String nombre, String apP, String apM, int sex, int des, int sobre, int aler, int obes, int diab, String otros)
    {
        char sexo = ' ';
        boolean desnutricion = false;
        boolean sobrepeso = false;
        boolean alergias = false;
        boolean obesidad = false;
        boolean diabetes = false;

        switch (sex)
        {
            case 1:
                sexo = 'M';
                break;
            case 2:
                sexo = 'F';
                break;
        }

        switch (des)
        {
            case 2:
                desnutricion = false;
                break;
            case 3:
                desnutricion = true;
                break;
        }
        switch (sobre)
        {
            case 2:
                sobrepeso = false;
                break;
            case 3:
                sobrepeso = true;
                break;
        }
        switch (aler)
        {
            case 2:
                alergias = false;
                break;
            case 3:
                alergias = true;
                break;
        }
        switch (obes)
        {
            case 2:
                obesidad = false;
                break;
            case 3:
                obesidad = true;
                break;
        }
        switch (diab)
        {
            case 2:
                diabetes = false;
                break;
            case 3:
                diabetes = true;
                break;
        }
        Alumnos obj = new Alumnos(viveCon, carrera, clave, nombre, apP, apM, sexo, desnutricion, sobrepeso, alergias, obesidad, diabetes, otros);
        return obj;
    }


