    private void masConsultasActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_masConsultasActionPerformed
    {//GEN-HEADEREND:event_masConsultasActionPerformed
        int[] arrFrec = null;
        int maxFrec = 0;
        String s = "";
        for (int i = 0; i < VtnPrincipal.matH.length; i++)
        {
            if (VtnPrincipal.matH[i].length > maxFrec)
            {

                maxFrec = VtnPrincipal.matH[i].length;
                arrFrec = new int[]
                {
                    i
                };
            } else if (VtnPrincipal.matH[i].length == maxFrec && VtnPrincipal.matH[i].length > 0)
            {
                // Si hay varios con la misma frecuencia máxima, agrega a arrFrec
                int[] nvo = new int[arrFrec.length + 1];
                System.arraycopy(arrFrec, 0, nvo, 0, arrFrec.length);
                nvo[arrFrec.length] = i;
                arrFrec = nvo;
            }
        }

        if (arrFrec != null)
        {
            s = ("Persona(s) con mayor frecuencia de consultas médicas\n");
            s += ("Clave\tNombre\tApellidoPaterno\t\tApellidoMaterno\t\tNo Consultas\n");
            for (int i : arrFrec)
            {
                s += VtnPrincipal.arrDatos[i].getCve() + "\t" + VtnPrincipal.arrDatos[i].getNom() + "\t" + VtnPrincipal.arrDatos[i].getPrimerAp() + "\t\t" + VtnPrincipal.arrDatos[i].getSegundoAp() + "\t\t" + maxFrec + "\n";
            }
            texto.setText(s);
        } else
        {
            texto.setText("Aun no hay consultas registradas");
        }
    }//GEN-LAST:event_masConsultasActionPerformed

    private void menosConsultasActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_menosConsultasActionPerformed
    {//GEN-HEADEREND:event_menosConsultasActionPerformed
        int[] arrMenorFrec = null;
        int minFrec = Integer.MAX_VALUE;
        String s = "";
        for (int i = 0; i < VtnPrincipal.matH.length; i++)
        {
            if (VtnPrincipal.matH[i].length < minFrec)
            {
                minFrec = VtnPrincipal.matH[i].length;
                arrMenorFrec = new int[1];
                arrMenorFrec[0] = i;
            } else if (VtnPrincipal.matH[i].length == minFrec)
            {
                // Si hay varios con la misma frecuencia mínima, agrega a arrMenorFrec
                int[] nvo = new int[arrMenorFrec.length + 1];
                System.arraycopy(arrMenorFrec, 0, nvo, 0, arrMenorFrec.length);
                nvo[arrMenorFrec.length] = i;
                arrMenorFrec = nvo;
            }
        }

        if (arrMenorFrec != null)
        {
            s = ("-----Persona(s) con menor frecuencia de consultas médicas-----\n");
            s += ("Clave\tNombre\t\tApellidoPaterno\t\tApellidoMaterno\t\tNo Consultas\n");
            for (int i : arrMenorFrec)
            {
                s += (VtnPrincipal.arrDatos[i].getCve() + "\t" + VtnPrincipal.arrDatos[i].getNom() + "\t\t" + VtnPrincipal.arrDatos[i].getPrimerAp() + "\t\t" + VtnPrincipal.arrDatos[i].getSegundoAp() + "\t\t" + minFrec + "\n");
            }
            texto.setText(s);
        }
    }//GEN-LAST:event_menosConsultasActionPerformed

