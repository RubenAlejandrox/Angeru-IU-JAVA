    private void aceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarActionPerformed
    {//GEN-HEADEREND:event_aceptarActionPerformed
        int pos;
        otros.setText(otros.getText().toUpperCase());
        try
        {

            if (ManipulacionProyecto.buscarCve(VtnPrincipal.arrDatos, clave.getText()) != -1)
            {

                if (sexo.getSelectedIndex() == 0 || desnutricion.getSelectedIndex() == 0 || sobrepeso.getSelectedIndex() == 0 || alergias.getSelectedIndex() == 0 || obesidad.getSelectedIndex() == 0 || diabetes.getSelectedIndex() == 0 || estatus.getSelectedIndex() == 0)
                {
                    Mensajes.error(this, "Porfavor, selecciona una opcion");

                } else
                {
                    if (clave.getText().equals("") || otros.getText().equals(""))
                    {
                        Mensajes.error(this, "La caja de texto esta vacia");
                    } else
                    {
                        pos = ManipulacionProyecto.buscarCve(VtnPrincipal.arrDatos, clave.getText());
                        VtnPrincipal.arrDatos = ManipulacionProyecto.modificarPersonal(VtnPrincipal.arrDatos, pos, clave.getText(), sexo.getSelectedIndex(), desnutricion.getSelectedIndex(), sobrepeso.getSelectedIndex(), alergias.getSelectedIndex(), obesidad.getSelectedIndex(), diabetes.getSelectedIndex(), otros.getText(), estatus.getSelectedIndex());
                        Mensajes.exito(this, "Datos modificados correctamente");
                        resetCombos();
                        cancelarActionPerformed(evt);
                    }

                }
            } else
            {
                Mensajes.error(this, "La clave no se encuentra registrada");
                cancelarActionPerformed(evt);
            }

        } catch (Exception e)
        {
            Mensajes.error(this, "Error..." + e.toString());
        }
    }//GEN-LAST:event_aceptarActionPerformed

