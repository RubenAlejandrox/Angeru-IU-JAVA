    private void aceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarActionPerformed
    {//GEN-HEADEREND:event_aceptarActionPerformed
        otros.setText(otros.getText().toUpperCase());
        try
        {

            if (ManipulacionProyecto.buscarCve(VtnPrincipal.arrDatos, clave.getText()) == -1)
            {

                if (sexo.getSelectedIndex() == 0 || desnutricion.getSelectedIndex() == 0 || sobrepeso.getSelectedIndex() == 0 || alergias.getSelectedIndex() == 0 || obesidad.getSelectedIndex() == 0 || diabetes.getSelectedIndex() == 0 || viveCon.getSelectedIndex() == 0 || carrera.getSelectedIndex() == 0)
                {
                    Mensajes.error(this, "Porfavor, selecciona una opcion");

                } else
                {
                    if (clave.getText().equals("") || nom.getText().equals("") || apP.getText().equals("") || apM.getText().equals("") || otros.getText().equals(""))
                    {
                        Mensajes.error(this, "La caja de texto esta vacia");
                    } else
                    {
                        VtnPrincipal.arrDatos = ManipulacionProyecto.agregarAlumno(VtnPrincipal.arrDatos, 1, true, asignarDatos(null, viveCon.getSelectedIndex(), carrera.getSelectedIndex(), clave.getText(), nom.getText(), apP.getText(), apM.getText(), sexo.getSelectedIndex(), desnutricion.getSelectedIndex(), sobrepeso.getSelectedIndex(), alergias.getSelectedIndex(), obesidad.getSelectedIndex(), diabetes.getSelectedIndex(), otros.getText()));
                        VtnPrincipal.matH = ManipulacionProyecto.agregarfila(VtnPrincipal.matH);
                        Mensajes.exito(this, "Datos registrados correctamente");
                        resetCombos();
                        cancelarActionPerformed(evt);

                    }

                }
            } else
            {
                Mensajes.error(this, "La clave ya se encuentra registrada");
                cancelarActionPerformed(evt);
            }

        } catch (Exception e)
        {
            Mensajes.error(this, "Error..." + e.toString());
        }
    }//GEN-LAST:event_aceptarActionPerformed

