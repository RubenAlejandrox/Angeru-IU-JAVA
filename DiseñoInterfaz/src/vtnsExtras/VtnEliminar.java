    public static HistorialClinico[][] eliminarFila(HistorialClinico[][] mat, int pos)
    {
        if (mat != null && pos >= 0 && pos < mat.length)
        {
            // Crear un nuevo arreglo con un tamaño menor
            HistorialClinico[][] nuevoMat = new HistorialClinico[mat.length - 1][];
 
            // Copiar las filas antes de la posición
            System.arraycopy(mat, 0, nuevoMat, 0, pos);
 
            // Copiar las filas después de la posición
            System.arraycopy(mat, pos + 1, nuevoMat, pos, mat.length - pos - 1);
 
            // Asignar el nuevo arreglo
            mat = nuevoMat;
        }
        return mat;
    }
    
    
    public static String imprimirPersona(HistorialClinico mat[][], Datos[] arr, int pos)
    {

        int cont = 0;
        String s = "";

        if (VtnPrincipal.arrDatos == null || VtnPrincipal.matH == null)
        {
            s = "No hay datos registrados aun";
        } else
        {
            if (pos == -1)
            {
                s = "La clave no se encuentra registrada";

            } else
            {

                s = "---PERSONA A ELIMINAR---\n";

                s += "    Nombre: "+arr[pos].getNom() + " " + arr[pos].getPrimerAp() + " " + arr[pos].getSegundoAp() + "\n";
                s += "    Sexo: " + arr[pos].getSexo() + "\n";
                s += "    Sobrepeso: " + arr[pos].isSobrepeso() + "\n";
                s += "    Alergias: " + arr[pos].isAlergias() + "\n";
                s += "    Obesidad: " + arr[pos].isObesidad() + "\n";
                s += "    Diabetes: " + arr[pos].isDiabetes() + "\n";
                s += "    Otros: " + arr[pos].getOtras() + "\n";
                s += "--------------------------------\n";
                cont++;
            }
        }

        return s;

    }

