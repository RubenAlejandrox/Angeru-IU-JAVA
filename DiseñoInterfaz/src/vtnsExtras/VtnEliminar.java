    private void aceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarActionPerformed
    {//GEN-HEADEREND:event_aceptarActionPerformed
        if (VtnPrincipal.arrDatos == null || VtnPrincipal.matH == null)
        {
            Mensajes.error(this, "No hay datos registrados");
        } else
        {
            int pos = ManipulacionProyecto.buscarCve(VtnPrincipal.arrDatos, clave.getText());
            if (pos != -1)
            {
                if (VtnPrincipal.arrDatos.length == 1)
                {
                    VtnPrincipal.arrDatos = null;
                } else
                {

                    // Crear un nuevo arreglo con un tamaño menor
                    Datos[] nuevoArreglo = new Datos[VtnPrincipal.arrDatos.length - 1];

                    // Copiar los elementos antes de la posición
                    System.arraycopy(VtnPrincipal.arrDatos, 0, nuevoArreglo, 0, pos);

                    // Copiar los elementos después de la posición
                    System.arraycopy(VtnPrincipal.arrDatos, pos + 1, nuevoArreglo, pos, VtnPrincipal.arrDatos.length - pos - 1);

                    // Asignar el nuevo arreglo
                    VtnPrincipal.arrDatos = nuevoArreglo;

                    // Asignar la nueva matriz
                    VtnPrincipal.matH = eliminarFila(VtnPrincipal.matH, pos);

                }

                Mensajes.exito(this, "Se ha eliminado");

            } else
            {
                Mensajes.error(this, "La clave no se encuentra registrada");
                cancelarActionPerformed(evt);
            }

        }

    }//GEN-LAST:event_aceptarActionPerformed

